    @classmethod
    def get_intro(cls): 
        if not cls.INTRO_MESSAGES: return "🔞 **The Red Room opens.**"
        return random.choice(cls.INTRO_MESSAGES)

    @classmethod
    def get_kill(cls, winner, loser, is_final=False):
        try:
            if is_final: 
                if not cls.FINAL_KILL_MESSAGES: return f"👑 **Winner:** {winner}"
                return random.choice(cls.FINAL_KILL_MESSAGES).format(winner=winner, loser=loser)
            
            if not (cls.OPENERS and cls.ACTIONS and cls.FINISHERS):
                return f"⚔️ {winner} dominated {loser}."
                
            o = random.choice(cls.OPENERS).format(winner=winner, loser=loser)
            a = random.choice(cls.ACTIONS).format(winner=winner, loser=loser)
            f = random.choice(cls.FINISHERS).format(winner=winner, loser=loser)
            return f"🔥 {o} {a} {f}"
        except Exception as e: 
            print(f"Lexicon Kill Error: {e}")
            return f"⚔️ {winner} dominated {loser}."

    @classmethod
    def get_legendary_event(cls, losers_names):
        try: 
            if not cls.LEGENDARY_EVENTS: return "⚠️ A chaotic event cleared several tributes."
            return random.choice(cls.LEGENDARY_EVENTS).format(losers=", ".join(str(l) for l in losers_names))
        except Exception as e: 
            print(f"Lexicon Event Error: {e}")
            return "⚠️ A chaotic event cleared several tributes."

    @classmethod
    def get_economy_msg(cls, cmd, user, reward):
        try: 
            if cmd not in cls.ECONOMY_MESSAGES or not cls.ECONOMY_MESSAGES[cmd]:
                return f"💰 {user} earned **{reward}** Flames through effort."
            return random.choice(cls.ECONOMY_MESSAGES[cmd]).format(user=user, reward=reward)
        except Exception as e: 
            print(f"Lexicon Economy Error: {e}")
            return f"💰 {user} earned **{reward}** Flames through effort."

    @classmethod
    def get_winner_announcement(cls, mention):
        try: 
            if not cls.WINNER_PINGS: return f"🏆 {mention} has won!"
            return random.choice(cls.WINNER_PINGS).format(mention=mention)
        except Exception as e: 
            print(f"Lexicon Win Error: {e}")
            return f"🏆 {mention} has won the Fiery Hangrygames Edition!"

    @classmethod
    def get_tension_msg(cls, u1_name, u2_name, count, pct):
        """Added helper for Gallery tension flavor."""
        return f"• {u1_name} 🔗 {u2_name}: {count} exchanges. **[{pct}% TENSION]**"
